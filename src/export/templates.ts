import type { GradientState, ColorStop } from '../state/GradientState';

// ── Shared helpers ────────────────────────────────────────────────────────────

function alphaHex(a: number): string {
  return a >= 1 ? '' : Math.round(a * 255).toString(16).padStart(2, '0');
}

function stateToConfig(s: GradientState): string {
  return JSON.stringify({
    colors:           s.colors,
    bgColor:          s.bgColor,
    renderMode:       s.renderMode,
    material:         s.material,
    motionMode:       s.motionMode,
    speed:            s.speed,
    seed:             s.seed,
    grain:            s.grain,
    glowAmount:       s.glowAmount,
    motionIntensity:  s.motionIntensity,
    iridescence:      s.iridescence,
    chromaticAberration: s.chromaticAberration,
    displaceFreqX:    s.displaceFreqX,
    displaceFreqZ:    s.displaceFreqZ,
    displaceAmount:   s.displaceAmount,
    twistFreqX: s.twistFreqX, twistFreqY: s.twistFreqY, twistFreqZ: s.twistFreqZ,
    twistPowX:  s.twistPowX,  twistPowY:  s.twistPowY,  twistPowZ:  s.twistPowZ,
    rotationX:  s.rotationX,  rotationY:  s.rotationY,  rotationZ:  s.rotationZ,
    scaleX:     s.scaleX,     scaleY:     s.scaleY,      scaleZ:     s.scaleZ,
  }, null, 2);
}

function cssMeshLayers(colors: ColorStop[]): string {
  return colors.map((c, i) => {
    const angle = (i / colors.length) * 360;
    const x = Math.round(50 + 38 * Math.cos(angle * Math.PI / 180));
    const y = Math.round(50 + 38 * Math.sin(angle * Math.PI / 180));
    return `  radial-gradient(circle at ${x}% ${y}%, ${c.hex}${alphaHex(c.alpha)}, transparent 65%)`;
  }).join(',\n');
}

