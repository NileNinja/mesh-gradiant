// ── Next.js component ─────────────────────────────────────────────────────────

export function genNextJs(s: GradientState): { component: string; loader: string; readme: string } {
  const config = stateToConfig(s);

  const component = `'use client';
/**
 * MeshGradient.tsx — generated by MeshStudio
 * Drop this + mesh-gradient-runtime.js into your Next.js project.
 * See README-nextjs.md for setup instructions.
 */
import { useEffect, useRef } from 'react';
import type { MeshGradientConfig } from './mesh-gradient-runtime';

export const MESH_CONFIG: MeshGradientConfig = ${config};

interface Props {
  className?: string;
  config?: Partial<MeshGradientConfig>;
  speed?: number;
}

export default function MeshGradient({ className = '', config, speed }: Props) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const rtRef = useRef<import('./mesh-gradient-runtime').MeshGradientRuntime | null>(null);

  useEffect(() => {
    let mounted = true;
    import('./mesh-gradient-runtime').then(({ MeshGradientRuntime }) => {
      if (!mounted || !canvasRef.current) return;
      const rt = new MeshGradientRuntime();
      rtRef.current = rt;
      rt.mount(canvasRef.current, {
        ...MESH_CONFIG,
        ...config,
        ...(speed !== undefined ? { speed } : {}),
      });
    });
    return () => {
      mounted = false;
      rtRef.current?.dispose();
    };
  }, []); // eslint-disable-line react-hooks/exhaustive-deps

  return (
    <canvas
      ref={canvasRef}
      aria-hidden="true"
      style={{ display: 'block', width: '100%', height: '100%' }}
      className={className}
    />
  );
}`;

  const loader = `// MeshGradientLoader.tsx — SSR-safe dynamic import wrapper
import dynamic from 'next/dynamic';
export const MeshGradient = dynamic(() => import('./MeshGradient'), { ssr: false });`;

  const readme = `# MeshGradient — Next.js Setup

## Files to copy into your project
1. MeshGradient.tsx
2. MeshGradientLoader.tsx
3. mesh-gradient-runtime.js  (from MeshStudio releases)

## Usage
\`\`\`tsx
// app/page.tsx
import { MeshGradient } from '@/components/MeshGradientLoader';

export default function Page() {
  return (
    <div className="relative min-h-screen">
      <MeshGradient className="absolute inset-0 -z-10" />
      <main>Your content here</main>
    </div>
  );
}
\`\`\`

## Runtime props
| Prop       | Type    | Description                     |
|------------|---------|---------------------------------|
| className  | string  | Tailwind / CSS classes          |
| config     | object  | Override any gradient config    |
| speed      | number  | Override animation speed (0-4)  |

## Features built-in
- Pauses automatically when scrolled off-screen (IntersectionObserver)
- Respects prefers-reduced-motion for accessibility
- Responsive resize with ResizeObserver
- DPR capped at 1.5x by default (set config.dprCap to change)
`;

  return { component, loader, readme };
}

