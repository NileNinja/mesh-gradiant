export function genVanilla(s: GradientState): string {
  const config = stateToConfig(s);
  const layers = cssMeshLayers(s.colors);

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8"/>
  <meta name="viewport" content="width=device-width,initial-scale=1"/>
  <title>Mesh Gradient</title>
  <style>
    *, *::before, *::after { box-sizing: border-box; margin: 0; padding: 0; }
    body { width: 100vw; height: 100vh; overflow: hidden; }
    #mesh-canvas { display: block; width: 100%; height: 100%; }
    .mesh-fallback {
      display: none; width: 100%; height: 100%;
      background: ${s.bgColor};
      background-image:
${layers};
    }
    @media (prefers-reduced-motion: reduce) {
      #mesh-canvas { display: none; }
      .mesh-fallback { display: block; }
    }
  </style>
</head>
<body>
  <canvas id="mesh-canvas" aria-hidden="true"></canvas>
  <div class="mesh-fallback" id="mesh-fallback" aria-hidden="true"></div>

  <script type="module">
    import { MeshGradientRuntime } from './mesh-gradient-runtime.js';

    const config = ${config};
    const canvas = document.getElementById('mesh-canvas');

    try {
      const rt = new MeshGradientRuntime();
      rt.mount(canvas, config);
    } catch (err) {
      console.warn('MeshGradient: WebGL2 unavailable, showing CSS fallback.', err);
      canvas.style.display = 'none';
      document.getElementById('mesh-fallback').style.display = 'block';
    }
  </script>
</body>
</html>`;
}

// ── Web Component ─────────────────────────────────────────────────────────────

export function genWebComponent(s: GradientState): string {
  const config = stateToConfig(s);

  return `/**
 * mesh-gradient.js — Web Component generated by MeshStudio
 *
 * Usage (any HTML/framework):
 *   <script type="module" src="mesh-gradient.js"></script>
 *   <mesh-gradient speed="1" render-mode="blobs"></mesh-gradient>
 *
 * Requires mesh-gradient-runtime.js in the same directory.
 */
import { MeshGradientRuntime } from './mesh-gradient-runtime.js';

const DEFAULT_CONFIG = ${config};

class MeshGradientElement extends HTMLElement {
  static get observedAttributes() {
    return ['colors', 'speed', 'seed', 'render-mode', 'material', 'motion-mode'];
  }

  constructor() {
    super();
    this._rt = null;
    this._canvas = null;
    const shadow = this.attachShadow({ mode: 'open' });
    // Build shadow DOM without innerHTML
    const canvas = document.createElement('canvas');
    canvas.setAttribute('aria-hidden', 'true');
    canvas.style.cssText = 'display:block;width:100%;height:100%';
    shadow.appendChild(canvas);
    this._canvas = canvas;
  }

  connectedCallback() {
    this._rt = new MeshGradientRuntime();
    this._rt.mount(this._canvas, this._buildConfig());
  }

  disconnectedCallback() {
    this._rt?.dispose();
    this._rt = null;
  }

  attributeChangedCallback() {
    this._rt?.update(this._buildConfig());
  }

  _buildConfig() {
    const cfg = { ...DEFAULT_CONFIG };
    const colors = this.getAttribute('colors');
    if (colors) { try { cfg.colors = JSON.parse(colors); } catch {} }
    const speed = this.getAttribute('speed');
    if (speed !== null) cfg.speed = parseFloat(speed);
    const seed  = this.getAttribute('seed');
    if (seed !== null) cfg.seed = parseFloat(seed);
    const rm  = this.getAttribute('render-mode');
    if (rm) cfg.renderMode = rm;
    const mat = this.getAttribute('material');
    if (mat) cfg.material = mat;
    const mm  = this.getAttribute('motion-mode');
    if (mm) cfg.motionMode = mm;
    return cfg;
  }
}

customElements.define('mesh-gradient', MeshGradientElement);`;
}

